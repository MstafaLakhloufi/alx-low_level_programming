#include "3-calc.h"

/**
* op_add - add two integers
* @a: first int
* @b: second int
*
* Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
* op_sub - subtracts two integers
* @a: first int
* @b: second int
*
* Return: sum
*/

int op_sub(int a, int b)
{
	return (a - b);
}


/**
* op_mul - multuplies two integers
* @a: first int
* @b: second int
*
* Return: sum
*/

int op_mul(int a, int b)
{
	return (a * b);
}


/**
* op_div - divides two integers
* @a: first int
* @b: second int
*
* Return: the quotient
*/

int op_div(int a, int b)
{
	return (a / b);
}


/**
* op_mod - modulo two integers
* @a: first int
* @b: second int
*
* Return: remainder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
